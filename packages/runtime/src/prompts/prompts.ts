export function getAnyPromptPresetById(
  id: string,
  savedPresets: SavedPromptPreset[],
): PromptPreset | SavedPromptPreset | undefined {
  return savedPresets.find((preset) => preset.id === id) ?? getBuiltinPromptPresetById(id);
}
